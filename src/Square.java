/**
 * The Square class is a special type of rectangle.
 * It is a TwoDimensionalShape and has a area
 * It has an equal length and a width
 */
public class Square extends Rectangle {

    /**
     * Creates a square
     * @param length    Represents the length of a side.
     */
    public Square(double length) {
        super(length, length);
    }

}
